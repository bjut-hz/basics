// @lc code=start
/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode(int x) : val(x), left(NULL), right(NULL) {}
 * };
 */
class Solution {
public:
    int rob(TreeNode* root) {
        map<TreeNode*, int> m;
        
        return rob(root, m);
    }

    int rob(TreeNode* root, map<TreeNode*, int>& mem) {
        if(nullptr == root) return 0;

        if(mem.count(root)) return mem[root];        
        int val = 0;
        if(root->left) {
            val += rob(root->left->left, mem);
            val += rob(root->left->right, mem);
        }
        if(root->right) {
            val += rob(root->right->left, mem);
            val += rob(root->right->right, mem);
        }

        int res = max(root->val + val, rob(root->left, mem) + rob(root->right, mem));
        mem[root] = res;
        return res;
    }
};
